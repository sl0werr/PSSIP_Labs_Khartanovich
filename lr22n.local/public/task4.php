<?php
function task4()
{
// 1. Создаем ассоциативный массив "Продукты питания"
$products = [
    'Хлеб' => 5000,
    'Молоко' => 800,
    'Сметана' => 7000
];

// 2. Выводим значение массива по ключу "Молоко"
echo "Стоимость Молока: " . $products['Молоко'] . "<br><br>";

// 3. Выводим исходный массив (для наглядности)
echo "Исходный массив:<br>";
print_r($products);
echo "<br><br>";

// 4. Сортируем массив в порядке убывания стоимости (по значениям)
arsort($products); // arsort() - сортирует ассоциативный массив по значениям в обратном порядке, сохраняя ключи

// 5. Выводим отсортированный массив
echo "Массив после сортировки по убыванию стоимости:<br>";
print_r($products);
echo "<br>";

// 6. Выводим в более красивом виде (для наглядности)
echo "<br>Отсортированный список продуктов (от дорогих к дешевым):<br>";
foreach ($products as $product => $price) {
    echo "$product: $price руб.<br>";
}

}
